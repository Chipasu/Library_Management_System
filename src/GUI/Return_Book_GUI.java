    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
       try
       {
           if (refno.getText().equals("")) 
         {
            JOptionPane.showMessageDialog(this, "Please enter the Reference Number ","",JOptionPane.WARNING_MESSAGE);
         }
           else
           {
               ref=Integer.parseInt(refno.getText());
               
                Connection con = DB.myCon();
                Statement S = con.createStatement();
                ResultSet rs=null;
                String statues="";
                rs = S.executeQuery("SELECT * FROM borrow WHERE refno=" +ref+ "");
                if(rs.next())
                {
                     mid=rs.getString("memberid");
                     bid=rs.getString("bookid");
                    Date due=rs.getDate("duedate");
                    statues=rs.getString("statues");
                    if(statues.equals("Borrowed"))
                   
                    {
                    
                        if(now.compareTo(due)>0)
                        {
                        
                        late.setEnabled(true);
                        fine.setEnabled(true);
                        enabling();
                        
                        
                        long MILLIS_PER_DAY = 24 * 3600 * 1000;
                        long msDiff= now.getTime() - due.getTime();
                        long daysDiff = Math.round(msDiff / ((double)MILLIS_PER_DAY));
                        
                        double fineammout=daysDiff*0.25;
                        
                        memberid.setText(""+mid);
                        bookid.setText(""+bid);
                        late.setText(""+daysDiff);
                        fine.setText(""+fineammout);
                        
                        DOD.setDate(due);
                        DOR.setDate(now);
                        JOptionPane.showMessageDialog(this, " Book is over due!","",JOptionPane.ERROR_MESSAGE);
                        }
                        else
                        {
                        enabling();
                        memberid.setText(""+mid);
                        bookid.setText(""+bid);
                        DOD.setDate(due);
                        DOR.setDate(now);
                        }
                    }
                   else
                    {
                        JOptionPane.showMessageDialog(this, " Book record is invilid !","",JOptionPane.ERROR_MESSAGE);
                    }
           }
           }
       }
       catch (NumberFormatException e1)
       {
            JOptionPane.showMessageDialog(this, "Only enter numbers to the relavant fields, !","",JOptionPane.WARNING_MESSAGE);
                
       }
       catch (Exception e)
       {
            JOptionPane.showMessageDialog(this, ""+e.getMessage()+" Error, !","",JOptionPane.OK_OPTION);
                
       }

    }//GEN-LAST:event_checkActionPerformed

