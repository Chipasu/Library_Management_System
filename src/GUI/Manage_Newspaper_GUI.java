    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
       int b = JOptionPane.showConfirmDialog(null, "Do you want to Update ?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (b == 0) {
        
        try {



            if (pricetxt.getText().equals("") || noofnewstxt.getText().equals("") || (sinhalaselect.getText().equals("") && englishselect.getText().equals("")) || Date.getDate() == null)
            {
                JOptionPane.showMessageDialog(this, "Please fill relavant fields ", "", JOptionPane.WARNING_MESSAGE);
            }
            else if((Double.parseDouble(pricetxt.getText())<0))
             {
            JOptionPane.showMessageDialog(this, "Please fill the price field correctly ", "",JOptionPane.WARNING_MESSAGE);
             }
               else if((Integer.parseInt(noofnewstxt.getText())<0))
             {
            JOptionPane.showMessageDialog(this, "Please fill the No of Newspapers field correctly ", "",JOptionPane.WARNING_MESSAGE);
             }
            else
               {

            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date date = Date.getDate();
            String ndate = (String) dateFormat.format(date);
            double price = Double.parseDouble(pricetxt.getText());
            int newspapercount = Integer.parseInt(noofnewstxt.getText()); 
            String sinhalanew =  sinhalaselect.getText();
            String englishnew =  englishselect.getText();
            
            
            Connection con = DB.myCon();
            Statement stmt = con.createStatement();
                String query1 = "UPDATE newspapers SET Sinhala_Paper='"+sinhalanew+"',English_Paper='"+englishnew+"', Ammount="+newspapercount+" , Price="+price+" WHERE Date = '" + ndate + "'";
                stmt.executeUpdate(query1);





                JOptionPane.showMessageDialog(this, "Record updated Successfully");


                reseting();
                disabling();
            }
        
        }
        
        
        catch (NumberFormatException n) 
             {
          JOptionPane.showMessageDialog(this, "  Only Enter Numbers to No of Newspapers & Price fields!","",JOptionPane.ERROR_MESSAGE);
            }


        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "" + e.getMessage() + " Error, !", "", JOptionPane.OK_OPTION);
        }
        }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
         int b = JOptionPane.showConfirmDialog(null, "Do you want to Delete ?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (b == 0) {
        try {
           if (Date.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Please enter the Date you wish to Delete ", "", JOptionPane.OK_OPTION);
            } else
            {

            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date dateadded=Date.getDate();
            String date=(String) dateFormat.format(dateadded);

            Connection con = DB.myCon();
            Statement s = con.createStatement();






            String q1 = ("DELETE FROM newspapers WHERE Date='" + date + "'");
            s.executeUpdate(q1);

            JOptionPane.showMessageDialog(null, "Record successfully Deleted");

            reseting();
            disabling();
            Date.setDate(null);




        
        }
        }
        
        
            catch (NumberFormatException n) 
            {
                JOptionPane.showMessageDialog(this, " Error Enter Numbers to relavant fields!","",JOptionPane.ERROR_MESSAGE);
            }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "" + e.getMessage() + " Error,Cannot be deleted !", "", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_deleteActionPerformed

