    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try {

            if (Date.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Please enter the Date you wish to Search ", "", JOptionPane.WARNING_MESSAGE);
            } else
            {

DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date dateadded=Date.getDate();
            String date=(String) dateFormat.format(dateadded);
                
                Connection con = DB.myCon();
                Statement S = con.createStatement();
                ResultSet rs = null;


                
                rs = S.executeQuery("SELECT * FROM newspapers WHERE Date='" +date+ "'");
                if (rs.next()) {
                        enabling();

                    sinhalaselect.setText(rs.getString("Sinhala_Paper"));

                    englishselect.setText(rs.getString("English_Paper"));

                    Date.setDate(rs.getDate("Date"));
                    pricetxt.setText(rs.getString("Price"));
                    noofnewstxt.setText(rs.getString("Ammount"));



                } 
                else 
                {
                    JOptionPane.showMessageDialog(this, " No such record  !", "", JOptionPane.ERROR_MESSAGE);
                    Date.setDate(null);
                    disabling();
                }
            }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "" + e.getMessage() + " Error, !", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchActionPerformed

