    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        int b = JOptionPane.showConfirmDialog(null, "Do you want to Update ?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (b == 0) { 
        try
        {
            String type= (String) typetxt.getSelectedItem();
        if (nametxt.getText().equals("") || inventoryidtxt.getText().equals("")||type.equals("Select")||ammounttxt.getText().equals("")||addeddate.getDate()==null) 
        {
              JOptionPane.showMessageDialog(this, "Please fill in all fields ", "",JOptionPane.WARNING_MESSAGE);
        }
        
         else if((Integer.parseInt(ammounttxt.getText())<0))
             {
            JOptionPane.showMessageDialog(this, "Please fill the  Ammount field correctly ", "",JOptionPane.WARNING_MESSAGE);
             }
        
        else if(inventoryidtxt.getText().charAt(0)=='I')
        {
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date d=addeddate.getDate();
            
            String inventory=inventoryidtxt.getText();
              
                String name=nametxt.getText();
                
                int ammount=Integer.parseInt(ammounttxt.getText());
                String addeddate1=(String) dateFormat.format(d);
        
       
        
            Connection con = DB.myCon();
            Statement stmt = con.createStatement();
            String query1=  "UPDATE inventory SET name = '"+name+"', type ='"+type+"', ammout="+ammount+" , Added_Date='"+addeddate1+"' WHERE inventoryid = '"+inventory+"'";
            stmt.executeUpdate(query1);
            
           
            
         
           
           JOptionPane.showMessageDialog(this,"Updated book Details Successfully");
           
           
           reseting();
           disabling();
        }
        
        else
        {
            JOptionPane.showMessageDialog(this, "Please enter a correct ID ","",JOptionPane.WARNING_MESSAGE);
        }
              
        
        
        }
       catch (NumberFormatException n) 
        {
     JOptionPane.showMessageDialog(this, " Only Enter Numbers to Ammount fields!","",JOptionPane.ERROR_MESSAGE);
        } 
       
         catch (Exception e) {
                    JOptionPane.showMessageDialog(this, ""+e.getMessage()+" Error, !","",JOptionPane.ERROR_MESSAGE);
                }
        }
    }//GEN-LAST:event_updateActionPerformed

